#!/bin/bash
# 다양한 렌더링 상황 테스트 실행 스크립트
# 
# 3가지 스크롤 시나리오를 테스트합니다:
# 1. 점진적 스크롤 (2초 대기) - 일반적인 사용자 패턴
# 2. 빠른 스크롤 (500ms 대기) - 빠르게 훑어보는 패턴
# 3. 매우 빠른 스크롤 (200ms 대기) - 극한 상황
#
# PDF vs Queue 버전을 비교합니다.

echo "🚀 렌더링 시나리오 테스트 시작"
echo "================================"
echo ""

# 실행 횟수 (기본값: 5회)
RUNS=${1:-5}

echo "⚙️  설정:"
echo "   - 실행 횟수: ${RUNS}회"
echo "   - 시나리오: 점진적(2s), 빠른(500ms), 매우 빠른(200ms)"
echo "   - 비교 버전: PDF vs Queue"
echo ""

# 현재 스크립트 디렉토리로 이동
cd "$(dirname "$0")"

# 테스트 실행
node pdf-advanced-benchmark.js ${RUNS}

echo ""
echo "✅ 렌더링 시나리오 테스트 완료!"
echo "📁 결과 파일: ./results/"

